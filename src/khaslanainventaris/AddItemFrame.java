    private void addItemBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addItemBtnActionPerformed
       try {
        // Ambil input
        int selectedIndex = categoryComboBox.getSelectedIndex();
        int categoryId = categoryIdList.get(selectedIndex);

        String code = codeField.getText().trim();
        String name = nameField.getText().trim();
        int jumlah = (int) jumlahSpinner.getValue();
        String condition = conditionComboBox.getSelectedItem().toString();
        String notes = noteTextArea.getText().trim();

        // VALIDASI

        // Kategori belum dipilih
        if (selectedIndex == -1 || categoryId == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih kategori!");
            return;
        }

        // Kondisi belum dipilih (index 0 = placeholder)
        if (conditionComboBox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Silakan pilih kondisi barang!");
            return;
        }

        // Field wajib
        if (code.isEmpty() || name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kode dan Nama barang wajib diisi!");
            return;
        }

        
        // CEK KODE UNIK
        String checkQuery = "SELECT COUNT(*) FROM items WHERE code = ?";
        PreparedStatement checkPs = dbConn.conn.prepareStatement(checkQuery);
        checkPs.setString(1, code);
        ResultSet rs = checkPs.executeQuery();

        if (rs.next() && rs.getInt(1) > 0) {
            JOptionPane.showMessageDialog(this, "Kode barang sudah digunakan!");
            return;
        }

        rs.close();
        checkPs.close();

        
        // INSERT DATA
        String insertQuery = """
            INSERT INTO items 
            (category_id, code, name, jumlah, item_condition, notes) 
            VALUES (?, ?, ?, ?, ?, ?)
        """;

        PreparedStatement ps = dbConn.conn.prepareStatement(insertQuery);

        ps.setInt(1, categoryId);
        ps.setString(2, code);
        ps.setString(3, name);
        ps.setInt(4, jumlah);
        ps.setString(5, condition);

        if (notes.isEmpty()) {
            ps.setNull(6, java.sql.Types.VARCHAR);
        } else {
            ps.setString(6, notes);
        }

        ps.executeUpdate();
        ps.close();

        //SUCCESS
        JOptionPane.showMessageDialog(this, "Item berhasil ditambahkan!");

        // 🔄 Reset form
        categoryComboBox.setSelectedIndex(0);
        conditionComboBox.setSelectedIndex(0);
        codeField.setText("");
        nameField.setText("");
        jumlahSpinner.setValue(1);
        noteTextArea.setText("");

        // Refresh table di main menu
        parent.refreshData();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menambahkan item\n" + e.getMessage());
        }
    }//GEN-LAST:event_addItemBtnActionPerformed

