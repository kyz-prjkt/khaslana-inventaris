    private void loadCategoryComboBox() {
        try {
            String query = "SELECT * FROM categories";

            PreparedStatement ps = dbConn.conn.prepareStatement(query);
            ResultSet rs = ps.executeQuery();

            categoryComboBox.removeAllItems();
            categoryIdList.clear();

            // Placeholder
            categoryComboBox.addItem("Pilih Kategori");
            categoryIdList.add(-1); // dummy id

            while (rs.next()) {
                categoryComboBox.addItem(rs.getString("name"));
                categoryIdList.add(rs.getInt("id"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load kategori\n" + e.getMessage());
        }
    }
    
